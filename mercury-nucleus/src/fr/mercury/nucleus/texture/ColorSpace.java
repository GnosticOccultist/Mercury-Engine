package fr.mercury.nucleus.texture;

/**
 * <code>ColorSpace</code> enumerates all supported colorspace for an
 * {@link Image}.
 * 
 * @author GnosticOccultist
 */
public enum ColorSpace {

    /**
     * The linear color space.
     */
    LINEAR,
    /**
     * The sRGB color space.
     */
    sRGB;
}
