package fr.mercury.nucleus.input;

/**
 * <code>Axis</code> enumerates various input axis.
 * 
 * @author GnosticOccultist
 */
public enum Axis {

    /**
     * The mouse X-axis, left/right displacement.
     */
    MOUSE_X,
    /**
     * The mouse Y-axis, up/down displacement.
     */
    MOUSE_Y;
}
